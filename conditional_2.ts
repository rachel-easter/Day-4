let money:number=300;

switch(money){
    case 1400:
        console.log("No need to pay tax");
        break;
    case 1500:
        console.log("need to pay tax");
        break;
    case 300:
        console.log("low balance");
        console.log("add money to card");
    case 2:
        console.log("two cases executed !");
        break;
    default:
        console.log("out of range");
        break;
        
}  




    
